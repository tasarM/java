// Demonstrate boolean data type
class DT_Boolean
{
	public static void main(String args[])
	{
		boolean a;
		a = false;
		System.out.println("a is " + a);
		
		a = true;
		System.out.println("a is " + a);
		
		if(a)
			System.out.println("a is true");
		
		a = false;
		if(a)
			System.out.println("a is false");
	}
}

