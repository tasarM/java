// Demonstrate if-else-if statements
class ifElse
{
	public static void main(String args[])
	{
		int i = 6;
		if(i == 5)
			System.out.println("i is 5");
		else if(i == 6)
			System.out.println("i is 6");
		else if(i == 7)
			System.out.println("i is 7");
		else
			System.out.println("i is not present");		
	}
}